    private void cargarClientes() {
        List<Cliente> listaClientes = clienteDAO.obtenerTodosLosClientes();
        DefaultTableModel model = (DefaultTableModel) tablaClientes.getModel();
        model.setRowCount(0); // Limpiar la tabla
        for (Cliente cliente : listaClientes) {
            model.addRow(new Object[]{cliente.getId(), cliente.getNombreCompleto(), cliente.getContacto(), cliente.getGenero()});
        }
    }


    private void buscarClientes() {
        String nombre = barraBusqueda.getText().trim();
        List<Cliente> listaClientes;
        if (nombre.isEmpty()) {
            listaClientes = clienteDAO.obtenerTodosLosClientes();
        } else {
            listaClientes = clienteDAO.buscarClientesPorNombre(nombre);
        }
        DefaultTableModel model = (DefaultTableModel) tablaClientes.getModel();
        model.setRowCount(0); // Limpiar la tabla
        for (Cliente cliente : listaClientes) {
            model.addRow(new Object[]{cliente.getId(), cliente.getNombreCompleto(), cliente.getContacto(), cliente.getGenero()});
        }
    }

