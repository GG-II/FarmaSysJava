    private void btnFinalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFinalizarMouseClicked
        if (validarCampos()) {
            Cliente nuevoCliente = new Cliente();
            nuevoCliente.setPrimerNombre(txtNombre1.getText().trim().toUpperCase());
            nuevoCliente.setSegundoNombre(txtNombre2.getText().trim().toUpperCase());
            nuevoCliente.setPrimerApellido(txtApellido1.getText().trim().toUpperCase());
            nuevoCliente.setSegundoApellido(txtApellido2.getText().trim().toUpperCase());
            nuevoCliente.setContacto(txtContacto.getText().trim());
            nuevoCliente.setGenero((String) comboGenero.getSelectedItem());

            if (cliente == null) {
                clienteDAO.agregarCliente(nuevoCliente);
            } else {
                nuevoCliente.setId(cliente.getId());
                clienteDAO.actualizarCliente(nuevoCliente);
            }
            mainFrame.ShowJPanel(new ClienteMenu(mainFrame));
        }
    }//GEN-LAST:event_btnFinalizarMouseClicked

    private boolean validarCampos() {
        if (txtNombre1.getText().trim().isEmpty() || txtApellido1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El primer nombre y el primer apellido son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    

