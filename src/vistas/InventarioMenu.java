    // Método para cargar todas las medicinas en la tabla
    private void cargarMedicinas() {
        List<Medicina> listaMedicinas = medicinaDAO.obtenerTodasLasMedicinas();
        DefaultTableModel modelo = (DefaultTableModel) tablaMedicinas.getModel();
        modelo.setRowCount(0); // Limpiar la tabla
        for (Medicina medicina : listaMedicinas) {
            Object[] fila = new Object[]{
                medicina.getId(),
                medicina.getNombre(),
                medicina.getDosis(),
                medicina.getPrecio(),
                medicina.getCantidad(),
                medicina.getFechaCaducidad(),
                medicina.getTipo()
            };
            modelo.addRow(fila);
        }
    }

    // Método para buscar medicinas por nombre y actualizar la tabla
    private void buscarMedicinas(String nombre) {
        List<Medicina> listaMedicinas = medicinaDAO.buscarMedicinasPorNombre(nombre);
        DefaultTableModel modelo = (DefaultTableModel) tablaMedicinas.getModel();
        modelo.setRowCount(0); // Limpiar la tabla
        for (Medicina medicina : listaMedicinas) {
            Object[] fila = new Object[]{
                medicina.getId(),
                medicina.getNombre(),
                medicina.getDosis(),
                medicina.getPrecio(),
                medicina.getCantidad(),
                medicina.getFechaCaducidad(),
                medicina.getTipo()
            };
            modelo.addRow(fila);
        }
    }
    
    

