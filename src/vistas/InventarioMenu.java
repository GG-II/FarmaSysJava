    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        int selectedRow = tablaMedicinas.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tablaMedicinas.getValueAt(selectedRow, 0); // Obtener el ID de la medicina seleccionada
            Medicina medicina = medicinaDAO.obtenerMedicinaPorId(id); // Obtener el objeto Medicina de la base de datos
            mainFrame.ShowJPanel(new MedicinaEdit(mainFrame, medicina)); // Llamar al constructor con la medicina seleccionada
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una medicina para editar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarMouseClicked


