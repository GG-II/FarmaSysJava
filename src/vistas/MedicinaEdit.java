    // Método para cargar los tipos de medicina en el comboTipo
    private void cargarTiposDeMedicina() {
        List<String> tiposDeMedicina = medicinaDAO.obtenerTiposDeMedicina();
        for (String tipo : tiposDeMedicina) {
            comboTipo.addItem(tipo);
        }
    }

    // Método para cargar los datos de la medicina en los campos de texto
    private void cargarDatosMedicina() {
        if (medicina != null) {
            txtNombre.setText(medicina.getNombre());
            txtDosis.setText(medicina.getDosis());
            txtPrecio.setText(String.valueOf(medicina.getPrecio()));
            txtCantidad.setText(String.valueOf(medicina.getCantidad()));

            // Convertir java.sql.Date a LocalDate
            java.sql.Date sqlDate = (java.sql.Date) medicina.getFechaCaducidad();
            LocalDate fechaCaducidad = sqlDate.toLocalDate();

            comboMes.setSelectedIndex(fechaCaducidad.getMonthValue() - 1); // Los meses son de 1 a 12, el índice es de 0 a 11
            comboAnio.setSelectedItem(String.valueOf(fechaCaducidad.getYear()));

            // Ajustar la selección del tipo de medicina
            comboTipo.setSelectedIndex(medicina.getTipoID() - 1); // Asumiendo que los tipos de medicina están ordenados por ID

            txtDescripcion.setText(medicina.getDescripcion());
        }
    }
    
    
    private LocalDate obtenerFechaCaducidad() {
        int mes = comboMes.getSelectedIndex() + 1; // Los meses en LocalDate son de 1 a 12
        int anio = Integer.parseInt((String) comboAnio.getSelectedItem());
        return LocalDate.of(anio, mes, 1); // El día es siempre el primero
    }

