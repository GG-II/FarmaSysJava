    private void btnFinalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFinalizarMouseClicked
        if (validarCampos()) {
            Medicina nuevaMedicina = new Medicina();
            nuevaMedicina.setNombre(txtNombre.getText().trim().toUpperCase());
            nuevaMedicina.setDosis(txtDosis.getText().trim());
            nuevaMedicina.setPrecio(Double.parseDouble(txtPrecio.getText().trim()));
            nuevaMedicina.setCantidad(Integer.parseInt(txtCantidad.getText().trim()));
            nuevaMedicina.setFechaCaducidad(java.sql.Date.valueOf(obtenerFechaCaducidad())); // Usar la fecha obtenida
            nuevaMedicina.setTipoID(comboTipo.getSelectedIndex() + 1);
            nuevaMedicina.setDescripcion(txtDescripcion.getText().trim());

            if (medicina == null) {
                medicinaDAO.agregarMedicina(nuevaMedicina);
            } else {
                nuevaMedicina.setId(medicina.getId());
                medicinaDAO.actualizarMedicina(nuevaMedicina);
            }
            JOptionPane.showMessageDialog(this, "Medicina guardada correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            mainFrame.ShowJPanel(new InventarioMenu(mainFrame));
        }
    }//GEN-LAST:event_btnFinalizarMouseClicked


    
